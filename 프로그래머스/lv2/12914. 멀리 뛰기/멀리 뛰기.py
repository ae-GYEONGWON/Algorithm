def solution(n):
    
    # n = 1                 1 (1)
    # n = 2                 11, 2(2)
    # n = 3                 111,12,21(3)
    # n = 4                 1111, 112, 121,211,22(5)
    # n = 5                 11111, 1112, 1121, 1211, 2111, 122, 212, 221(8)
    
    dp = [0]*2001
    dp[1] = 1; dp[2] = 2
    
    if n < 3:
        return n
    else:
        for i in range(3, n+1):
            dp[i] = dp[i-1] + dp[i-2]
        
        return dp[n]%1234567