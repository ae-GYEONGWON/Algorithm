n = int(input())

# 1 - 1
# 2 - 00, 11
# 3 - 100, 001, 111
# 4 - 0011, 0000, 1001, 1111, 1100
# 5 - 00111, 00001, 10011, 11001, 11100, 00100, 10000, 11111
# dp[1] = 1, dp[2] = 2, dp[3] = 3, dp[4] = 5, dp[5] = 8
# dp[n] = dp[n-1] + dp[n-2]

if n == 1:
    print(1)
else:
    dp = [0] * (n+1)
    dp[1] = 1; dp[2] = 2
    for i in range(3, n+1):
        dp[i] = (dp[i-1] + dp[i-2])%15746
    print(dp[n])